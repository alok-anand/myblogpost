            <h3>Cancellation or Reschedule Fee for IELTS Exam</h3>
            <p>In case you would like to cancel or reschedule your IELTS exam, you need to keep the following points in mind:</p>
            <ul>
              <li>You will receive a refund minus administration fees if you cancel or reschedule your IELTS test more than five weeks before the test date.</li>
              <li>Unless you have some major medical issues, you will be charged the entire cost for the test if you cancel or reschedule your IELTS within five weeks of the test date.</li>
              <li>If a medical certificate is presented within five days of the test date, you will be given a refund less any applicable local administrative fees.</li>
            </ul>
            <h3>How to register for the IELTS exam?</h3>
            <p>The process to register for the IELTS test is very simple. You need to keep your Passport handy while registering for the test and need to follow a few simple steps as follows:</p>
            <ol type="1">
              <li>Visit the official IDP website.</li>
              <li>Create an online account on this website for registration .</li>
              <li>Locate the nearest test center .</li>
              <li>Complete your registration details on the website .</li>
              <li>You may pay the fee online by Debit Card, Credit Card or even Netbanking .</li>
            </ol>
            <p>Once you have registered for the test, every student will receive a written confirmation from the test center. This confirmation will be about the test date and time of the test .</p>
