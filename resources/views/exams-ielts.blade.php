            <p>The most important query about the IELTS exam that comes to an aspirant’s mind is ‘What is the format of the IELTS exam?’ Well, the IELTS test is mainly structured into four sections – Reading, Listening, Writing and Speaking. This test assesses the ability of a person to comprehend, write and speak in the English language.</p>
            <p>The Listening, Reading and Writing tests are conducted on a single day, while the Speaking test is conducted on another day and date. The Speaking test slot can be booked online or offline as well on a day and date of the test taker’s preference. In case, the student does not book his speaking test, it is automatically booked for him. The total time of the IELTS exam is 2 hours and approximately 45 minutes for Listening, Reading and Writing tests. The Speaking test takes about 11 to 14 minutes to be conducted.</p>
            <h3>Different Versions of the IELTS Exam</h3>
            <p>The IELTS test has two different variations or versions:</p>
            <ol type="1">
              <li><b>IELTS Academic Version:</b> This version of the test is taken by students who wish to study in an educational institute in an English-speaking nation like Canada, New Zealand, Australia or the UK.</li>
              <li><b>IELTS General Training Version:</b> This version of the test is taken by persons who wish to migrate or work in a nation like Canada, New Zealand, Australia or the United Kingdom etcetera.</li>
            </ol>
            <h3>IELTS Exam Syllabus for Academic and General Training Version</h3>
            <p>The IELTS exam has four different sections – Listening, Reading, Writing and Speaking. The IELTS exam is structured in different manners for both versions although it is assessed in the same manner.</p>
            <p>The Listening and Speaking sections are the same for both versions. There are differences in the Reading and Writing sections of the test. The Reading section for the General Training version consists of four Short Passages and one Long Passage, whereas the Academic Version consists of three Long Passages. Though the total number of questions is 40 in both versions of the test. For the Writing section, both versions have two Tasks. The General training version has a letter and a short essay, whereas the Academic version has a report and a short essay as two writing tasks.</p>
